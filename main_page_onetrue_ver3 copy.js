// 모달 메뉴 스타일 적용 시작
function modalMenuClick(clickedMenuId, contentId) {
    const modalMenus = document.getElementsByClassName('modal_nav');
    for (let i = 0; i < modalMenus.length; i++) {
        modalMenus[i].style.textDecorationLine = 'none';
    }
    const clickedMenu = document.getElementById(clickedMenuId);
    clickedMenu.style.textDecorationLine = 'underline';
    clickedMenu.style.textUnderlineOffset = '6px';
    clickedMenu.style.textDecorationThickness = '8px';

    // 모달 메뉴에 따라 내용 변경 파트
    const modalContents = document.querySelectorAll('.v-content');
    modalContents.forEach(content => {
        content.style.display = 'none';
    });
    const clickedContent = document.getElementById(contentId);
    clickedContent.style.display = 'block';
}

function closeModal(modalId) {
    document.getElementById(modalId).style.display = 'none';
    document.body.style.overflow = 'auto'; // 배경 페이지 스크롤 허용
}

document.querySelectorAll('.modal').forEach(modal => {
    modal.addEventListener('click', function (event) {
        if (event.target.classList.contains('modal')) {
            closeModal(modal.id);
            document.body.style.overflow = 'auto'; // 배경 페이지 스크롤 허용
        }
    });
});
