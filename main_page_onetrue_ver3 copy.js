
// 모달 열기
function openModal1(clickedMenuId) {
    const modalMenus = document.getElementsByClassName('modal_nav');
    for (let i = 0; i < modalMenus.length; i++) {
        modalMenus[i].style.textDecorationLine = 'none';
    }
    const modalContents = document.querySelectorAll('.v-content');
    modalContents.forEach(content => {
        content.style.display = 'none';
    });
    document.getElementById("myModal1").style.display = "block"; /* 모달 창을 보이게 합니다. */
    document.getElementById("modal_nav1_1").style.textDecorationLine = 'underline';
    document.getElementById("modal_nav1_1").style.textUnderlineOffset = '6px';
    document.getElementById("modal_nav1_1").style.textDecorationThickness = '8px';
    document.getElementById("modalMenusContent1_1").style.display = 'block';
    document.body.style.overflow = 'hidden'; // 배경 페이지 스크롤 막기
    
}

// 모달 닫기
function closeModal1() {
    document.getElementById("myModal1").style.display = "none"; /* 모달 창을 숨깁니다. */
    document.body.style.overflow = 'auto'; // 배경 페이지 스크롤 허용
}

